var TOTAL = 10;
var PASS = 6;
var LIMIT = 10 * 60;
var questions = [];
var current = 0;
var answers = [];
var remaining = LIMIT;
var timer = null;
var finished = false;

function $(id) { return document.getElementById(id); }
function shuffle(arr) {
  var a = arr.slice();
  for (var i = a.length - 1; i > 0; i--) {
    var j = Math.floor(Math.random() * (i + 1));
    var tmp = a[i]; a[i] = a[j]; a[j] = tmp;
  }
  return a;
}
function two(n) { return n < 10 ? '0' + n : '' + n; }
function formatTime(s) { return two(Math.floor(s / 60)) + ':' + two(s % 60); }
function selectedCount() {
  var count = 0;
  for (var i = 0; i < answers.length; i++) if (answers[i]) count++;
  return count;
}
function makeQuestion(item, idx) {
  return {
    q: item.q,
    c: item.c,
    w: item.w,
    img: item.img || '',
    id: idx,
    opts: shuffle([item.c].concat(item.w))
  };
}
function startQuiz() {
  var picked = shuffle(BANK).slice(0, TOTAL);
  questions = [];
  for (var i = 0; i < picked.length; i++) questions.push(makeQuestion(picked[i], i));
  current = 0;
  answers = [];
  for (var k = 0; k < TOTAL; k++) answers.push(null);
  remaining = LIMIT;
  finished = false;
  $('start').classList.add('hidden');
  $('finish').classList.add('hidden');
  $('quiz').classList.remove('hidden');
  $('review').classList.add('hidden');
  $('timerPill').style.color = '';
  $('timerPill').textContent = '⏱ ' + formatTime(remaining);
  if (timer) clearInterval(timer);
  timer = setInterval(tick, 1000);
  renderQuestion();
}
function tick() {
  remaining--;
  if (remaining < 0) remaining = 0;
  $('timerPill').textContent = '⏱ ' + formatTime(remaining);
  if (remaining <= 60) $('timerPill').style.color = 'var(--bad)';
  if (remaining <= 0) finishQuiz();
}
function renderQuestion() {
  var item = questions[current];
  $('counter').textContent = 'Вопрос ' + (current + 1) + ' из ' + TOTAL;
  $('topic').textContent = 'Выбрано ответов: ' + selectedCount() + '/' + TOTAL;
  $('bar').style.width = Math.round((current / TOTAL) * 100) + '%';
  $('question').textContent = item.q;

  var imageBox = $('qimage');
  imageBox.innerHTML = '';
  if (item.img) {
    var im = document.createElement('img');
    im.src = item.img;
    im.alt = 'Иллюстрация к вопросу';
    imageBox.appendChild(im);
    imageBox.classList.remove('hidden');
  } else {
    imageBox.classList.add('hidden');
  }

  var box = $('answers');
  box.innerHTML = '';
  for (var i = 0; i < item.opts.length; i++) {
    (function(opt) {
      var b = document.createElement('button');
      b.type = 'button';
      b.className = 'answer';
      b.textContent = opt;
      if (answers[current] === opt) b.classList.add('selected');
      b.addEventListener('click', function() { answers[current] = opt; renderQuestion(); }, false);
      b.addEventListener('touchend', function(e) { e.preventDefault(); answers[current] = opt; renderQuestion(); }, false);
      box.appendChild(b);
    })(item.opts[i]);
  }
  $('prevBtn').disabled = current === 0;
  $('nextBtn').textContent = current === TOTAL - 1 ? 'Завершить' : 'Далее';
}
function next() {
  if (current < TOTAL - 1) { current++; renderQuestion(); }
  else finishQuiz();
}
function prev() {
  if (current > 0) { current--; renderQuestion(); }
}
function finishQuiz() {
  if (finished) return;
  finished = true;
  if (timer) clearInterval(timer);
  var score = 0;
  for (var i = 0; i < TOTAL; i++) if (answers[i] === questions[i].c) score++;
  $('quiz').classList.add('hidden');
  $('finish').classList.remove('hidden');
  $('scoreText').textContent = score + '/' + TOTAL;
  $('passText').innerHTML = score >= PASS ? '<span class="good">Зачет пройден.</span>' : '<span class="bad">Нужно повторить материал.</span>';
  renderReview();
}
function renderReview() {
  var html = '<h3>Разбор ответов</h3>';
  for (var i = 0; i < questions.length; i++) {
    var q = questions[i];
    var ok = answers[i] === q.c;
    html += '<div class="review-item">';
    html += '<strong>' + (i + 1) + '. ' + escapeHtml(q.q) + '</strong>';
    if (q.img) html += '<img src="' + escapeHtml(q.img) + '" alt="Иллюстрация к вопросу">';
    html += '<p>Ваш ответ: <span class="' + (ok ? 'good' : 'bad') + '">' + escapeHtml(answers[i] || 'не выбран') + '</span></p>';
    if (!ok) html += '<p>Правильный ответ: <span class="good">' + escapeHtml(q.c) + '</span></p>';
    html += '</div>';
  }
  $('review').innerHTML = html;
}
function escapeHtml(text) {
  return String(text).replace(/[&<>"']/g, function(ch) {
    return {'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#039;'}[ch];
  });
}
function bindButton(id, fn) {
  var el = $(id);
  el.addEventListener('click', fn, false);
  el.addEventListener('touchend', function(e) { e.preventDefault(); fn(); }, false);
}
function init() {
  bindButton('startBtn', startQuiz);
  bindButton('nextBtn', next);
  bindButton('prevBtn', prev);
  bindButton('retryBtn', startQuiz);
  bindButton('reviewBtn', function() { $('review').classList.toggle('hidden'); });
}
if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', init, false);
else init();
